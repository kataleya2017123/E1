#include<iostream>
using namespace std;
int main()
{
  float k,p;
  cout<<"Ingrese k  : "; cin>>k;
  cout<<"Ingrese p  : "; cin>>p;
  if(k==p){
     cout<<"Son iguales"<<endl;
  }else{
     if(k<p){
       cout<<k<<" es el menor que "<<p<<"\n";
     }else{
       cout<<p<<" es el menor que "<<k<<"\n";
     }
  }

return (0);;






}
